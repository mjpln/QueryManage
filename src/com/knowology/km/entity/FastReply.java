package com.knowology.km.entity;

public class FastReply {
	private String type;
	private int id;
	private String Q;
	private String A;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQ() {
		return Q;
	}
	public void setQ(String q) {
		Q = q;
	}
	public String getA() {
		return A;
	}
	public void setA(String a) {
		A = a;
	}
	
}
